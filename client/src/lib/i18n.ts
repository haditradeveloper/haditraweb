export type Language = 'en' | 'ar';

export function useTranslation(lang: Language = 'en') {
  return {
    t: (key: string) => {
      const keys = key.split('.');
      let value: any = translations[lang];
      for (const k of keys) {
        value = value?.[k];
      }
      return value || key;
    },
    lang
  };
}
